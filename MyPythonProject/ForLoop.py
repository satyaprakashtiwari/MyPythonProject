# print 1 to 10 numbers
for i in range(10):
    print(i)


# print even number between 1 to 10 numbers
for i in range(2,10,2):     # (initial value, max value, increment/decrement value)
    print(i)

# print odd number between 1 to 10 numbers
for i in range(1,10,2):     # (initial value, max value, increment/decrement value)
    print(i)

# print numbers in descending order
for i in range(10,0,-1):
    print(i)